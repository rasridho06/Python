# Easy
for i in range(5):
    print("Menggambar lingkaran ke-", i+1)

# Medium
counter = 0
while counter < 5:
    print("Menggambar lingkaran ke-", counter+1)
    counter += 1

# Hard
colors = ["merah", "kuning", "hijau", "biru", "ungu"]
for color in colors:
    print("Menggambar lingkaran berwarna", color)
